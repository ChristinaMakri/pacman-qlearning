class TrainingController(GameController):
    """
    Extends GameController with a Q-learning agent that controls Pac-Man.

    Reward signal:
        +pellet.points  when a pellet is eaten  (10 or 50)
        +ghost.points   when a freight ghost is eaten (200, 400, …)
        -500            when Pac-Man dies
        -1              every node transition (encourages efficiency)

    The Q-table is saved to qtable.pkl after training.
    """

    # Rewards
    R_DEATH  = -500
    R_STEP   =   -1

    def __init__(self, train=True):
        super().__init__()
        self.agent = QLearningAgent()
        self.train_mode = train

        # Per-step accumulated reward (since last node arrival)
        self._step_reward = 0.0
        # Bookkeeping for Q-update
        self._last_state  = None
        self._last_action = None
        # Episode stats
        self._ep_total_reward = 0.0
        self._ep_count        = 0
        self._scores_log      = []
        self._rewards_log     = []

        if not train:
            # Play mode: load trained table, no exploration
            self.agent.load()
            self.agent.epsilon = 0.0

    # ------------------------------------------------------------------
    # Core loop override
    # ------------------------------------------------------------------

    def update(self):
        dt = self.clock.tick(30) / 1000.0
        self.textgroup.update(dt)
        self.pellets.update(dt)
        if not self.pause.paused:
            self.ghosts.update(dt)
            if self.fruit is not None:
                self.fruit.update(dt)
            self.checkPelletEvents()
            self.checkGhostEvents()
            self.checkFruitEvents()

        if self.pacman.alive:
            if not self.pause.paused:
                self.pacman.update(dt)
                # Agent decides whenever Pac-Man arrives at a new node
                if self.pacman.just_reached_node:
                    self._agent_decide()
        else:
            self.pacman.update(dt)

        if self.flashBG:
            self.flashTimer += dt
            if self.flashTimer >= self.flashTime:
                self.flashTimer = 0
                if self.background == self.background_norm:
                    self.background = self.background_flash
                else:
                    self.background = self.background_norm

        afterPauseMethod = self.pause.update(dt)
        if afterPauseMethod is not None:
            afterPauseMethod()
        self.checkEvents()
        self.render()

    # ------------------------------------------------------------------
    # Reward hooks (override parent event checks)
    # ------------------------------------------------------------------

    def checkPelletEvents(self):
        pellet = self.pacman.eatPellets(self.pellets.pelletList)
        if pellet:
            self.pellets.numEaten += 1
            self.updateScore(pellet.points)
            if self.pellets.numEaten == 30:
                self.ghosts.inky.startNode.allowAccess(RIGHT, self.ghosts.inky)
            if self.pellets.numEaten == 70:
                self.ghosts.clyde.startNode.allowAccess(LEFT, self.ghosts.clyde)
            self.pellets.pelletList.remove(pellet)
            if pellet.name == POWERPELLET:
                self.ghosts.startFreight()
            self._step_reward += pellet.points   # <-- reward signal
            if self.pellets.isEmpty():
                self.flashBG = True
                self.hideEntities()
                self._terminal_update()
                self.pause.setPause(pauseTime=3, func=self.nextLevel)

    def checkGhostEvents(self):
        for ghost in self.ghosts:
            if self.pacman.collideGhost(ghost):
                if ghost.mode.current is FREIGHT:
                    self.pacman.visible = False
                    ghost.visible = False
                    self.updateScore(ghost.points)
                    self.textgroup.addText(str(ghost.points), WHITE,
                                          ghost.position.x, ghost.position.y, 8, time=1)
                    self.ghosts.updatePoints()
                    self.pause.setPause(pauseTime=1, func=self.showEntities)
                    ghost.startSpawn()
                    self.nodes.allowHomeAccess(ghost)
                    self._step_reward += ghost.points  # <-- reward signal
                elif ghost.mode.current is not SPAWN:
                    if self.pacman.alive:
                        self.lives -= 1
                        self.lifesprites.removeImage()
                        self.pacman.die()
                        self.ghosts.hide()
                        self._step_reward += self.R_DEATH  # <-- reward signal
                        self._terminal_update()
                        if self.lives <= 0:
                            self.textgroup.showText(GAMEOVERTXT)
                            self._end_episode()
                            self.pause.setPause(pauseTime=3, func=self.restartGame)
                        else:
                            self.pause.setPause(pauseTime=3, func=self.resetLevel)

    # ------------------------------------------------------------------
    # Agent helpers
    # ------------------------------------------------------------------

    def _agent_decide(self):
        """Q-update for the previous step, then pick next action."""
        ghosts      = list(self.ghosts)
        state       = self.agent.get_state(self.pacman, ghosts, self.pellets)
        valid_dirs  = self.pacman.validDirections()

        if self._last_state is not None:
            self.agent.update(
                self._last_state, self._last_action,
                self._step_reward,
                state, valid_dirs
            )
            self._ep_total_reward += self._step_reward

        action = self.agent.choose_action(state, valid_dirs)
        self.pacman.ai_direction = action
        self._last_state  = state
        self._last_action = action
        self._step_reward = self.R_STEP   # reset with per-step penalty

    def _terminal_update(self):
        """Q-update with no next state (death or level clear)."""
        if self._last_state is not None:
            self.agent.update(
                self._last_state, self._last_action,
                self._step_reward,
                None, []
            )
            self._ep_total_reward += self._step_reward
        self._last_state  = None
        self._last_action = None
        self._step_reward = 0.0

    def _end_episode(self):
        """Called at true game-over (lives = 0). Log stats, decay epsilon."""
        self._ep_count += 1
        self._scores_log.append(self.score)
        self._rewards_log.append(self._ep_total_reward)
        print(
            f"Episode {self._ep_count:4d} | "
            f"Score {self.score:6d} | "
            f"Reward {self._ep_total_reward:8.1f} | "
            f"ε={self.agent.epsilon:.4f} | "
            f"States={len(self.agent.q_table)}"
        )
        self._ep_total_reward = 0.0
        if self.train_mode:
            self.agent.decay_epsilon()

    # ------------------------------------------------------------------
    # Override restartGame to intercept episode boundary
    # ------------------------------------------------------------------

    def restartGame(self):
        super().restartGame()
        self._last_state  = None
        self._last_action = None
        self._step_reward = 0.0

    # ------------------------------------------------------------------
    # Training loop
    # ------------------------------------------------------------------

    def train(self, num_episodes=500, save_every=100):
        """Run `num_episodes` full games and save the Q-table periodically."""
        self.startGame()
        self._ep_count = 0
        print(f"Training for {num_episodes} episodes …  (close window to stop early)")
        while self._ep_count < num_episodes:
            self.update()
            if self._ep_count > 0 and self._ep_count % save_every == 0:
                self.agent.save()
        self.agent.save()
        print("Training complete.")
        self._print_summary()

    def _print_summary(self):
        if not self._scores_log:
            return
        n = len(self._scores_log)
        recent = self._scores_log[max(0, n-50):]
        print(f"\n--- Summary ({n} episodes) ---")
        print(f"  Avg score (all):    {sum(self._scores_log)/n:.1f}")
        print(f"  Avg score (last 50): {sum(recent)/len(recent):.1f}")
        print(f"  Best score:          {max(self._scores_log)}")


